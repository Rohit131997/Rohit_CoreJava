
public class If 
{
    public static void main(String args[]) 
    { 
        int i = 20; 
  
        if (i > 15) 
            System.out.println("20 is less than 15"); 
  
        // This statement will be executed 
        // as if considers one statement by default 
        System.out.println("I am Not in if"); 
    } 
} 
