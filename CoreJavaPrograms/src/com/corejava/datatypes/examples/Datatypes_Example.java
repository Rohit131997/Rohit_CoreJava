public class Datatypes_Example {
	public static void main(String[] args) {
		int data1 = 23;
		short data2 = 19;
		long data3 = 29;
		float data4 = 36;
		char data5 = 'T';
		double data6 = 112;
		String data7 = "Hello";
		System.out.println("Integer value :" + data1);
		System.out.println("Short value :" + data2);
		System.out.println("Long value :" + data3);
		System.out.println("Float value :" + data4);
		System.out.println("Charecter value :" + data5);
		System.out.println("Double value :" + data6);
		System.out.println("String value :" + data7);
	}

}
